@testset "van Vleck Expansion" begin
    clear_caches!()
    H = Terms([Term(; rotating=0), Term(; rotating=1)])
    set_hamiltonian!(H)

    @test repr(K(1)) == "-1//2*[1/1,1]0"
    @test repr(K(2)) == "1//2*[[1/1,0]/1,1]0\n1//3*[[1/1,1]/1,1]0"
    @test repr(K(3)) ==
        "-1//2*[[[1/1,0]/1,0]/1,1]0\n-1//3*[[[1/1,0]/1,1]/1,1]0\n-1//4*[[[1/1,1]/1,0]/1,1]0\n-1//6*[[[1/1,1]/1,1]/1,1]0\n-1//24*[[1/1,[1/1,1]]/1,1]0\n-1//8*[[1/1,[1/1,1]0]/1,1]0\n-1//12*[[1/1,0]/1,[1/1,1]]0\n-1//24*[[1/1,1]/1,[1/1,1]]0"
    # https://doi.org/10.1103/PhysRevLett.134.060401
end
